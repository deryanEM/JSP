package com.model;


/**
 *
 * @author Dibaj
 */
public class Kelas {
    
    private String kodekelas;
    private String namakelas;
    private String jurusan;
    private String ketuakelas;
    private String walikelas;
    private String jumlahsiswa;

    public String getKode_kelas() {
        return kodekelas;
    }

    public void setKode_kelas(String kodekelas) {
        this.kodekelas = kodekelas;
    }

    public String getNama_kelas() {
        return namakelas;
    }

    public void setNama_kelas(String namakelas) {
        this.namakelas = namakelas;
    }
    
    public String getJurusan() {
        return jurusan;
    }

    public void setJurusan(String jurusan) {
        this.jurusan = jurusan;
    }

    public String getKetua() {
        return ketuakelas;
    }

    public void setKetua(String ketuakelas) {
        this.ketuakelas = ketuakelas;
    }

    public String getWali() {
        return walikelas;
    }

    public void setWali(String walikelas) {
        this.walikelas = walikelas;
    }

    public String getJml_Siswa() {
        return jumlahsiswa;
    }

    public void setJml_Siswa(String jumlahsiswa) {
        this.jumlahsiswa = jumlahsiswa;
    }
    
}
