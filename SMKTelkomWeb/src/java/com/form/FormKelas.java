    public void loadRecords() {
        try {
            DBConnection conn = DBConnection.getInstance();
            KelasQuery daoSiswa = new KelasQuery(conn.getCon());
            records = daoSiswa.getAll();
        } catch (SQLException ex) {
            Logger.getLogger(FormKelas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void updateRow() {
        if (this.records.size() > 0) {
            if (row < 0) {
                row = 0;
            }
            Kelas kl = records.get(row);
            txtKode.setText(kl.getKode_kelas());
            txtNama.setText(kl.getNama_kelas());
            txtJurusan.setText(kl.getJurusan());
            txtKetua.setText(kl.getKetua());
            txtWali.setText(kl.getWali());
            txtJumlah.setText(kl.getJml_Siswa());
        } else {
            bersihkanForm();
            editForm(false);
        }
    }

    void bersihkanForm() {
        txtKode.setText("");
        txtNama.setText("");
        txtJurusan.setText("");
        txtKetua.setText("");
        txtWali.setText("");
        txtJumlah.setText("");
    }

    void editForm(boolean x) {
        txtKode.setEditable(x);
        txtNama.setEditable(x);
        txtJurusan.setEditable(x);
        txtKetua.setEditable(x);
        txtWali.setEditable(x);
        txtJumlah.setText("");
    }

    public void loadRecords2() {
        try {
            DBConnection conn = DBConnection.getInstance();
            KelasQuery daoSiswa = new KelasQuery(conn.getCon());
            records = daoSiswa.getAllNis(txtKode.getText());
        } catch (SQLException ex) {
            Logger.getLogger(FormKelas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void updateRow2() {
        if (this.records.size() > 0) {
            if (row < 0) {
                row = 0;
            }
            Kelas kl = records.get(row);
            txtKode.setText(kl.getKode_kelas());
            txtNama.setText(kl.getNama_kelas());
            txtJurusan.setText(kl.getJurusan());
            txtKetua.setText(kl.getKetua());
            txtWali.setText(kl.getWali());
            txtJumlah.setText(kl.getJml_Siswa());
        } else {
            JOptionPane.showMessageDialog(this, "Not Found");
            loadRecords();
            updateRow();
        }
    }

