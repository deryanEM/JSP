    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            Kelas kl = new Kelas();
            kl.setKode_kelas(txtKode.getText());
            DBConnection conn = DBConnection.getInstance();
            KelasQuery dao = new KelasQuery(conn.getCon());
            dao.delete(txtKode.getText());
            JOptionPane.showMessageDialog(this, "Data Deleted");
            loadRecords();
            updateRow();
        } catch (SQLException ex) {
            Logger.getLogger(FormKelas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (status == true) {
            try {
                Kelas kl = new Kelas();
                kl.setKode_kelas(txtKode.getText());
                kl.setNama_kelas(txtNama.getText());
                kl.setJurusan(txtJurusan.getText());
                kl.setKetua(txtKetua.getText());
                kl.setWali(txtWali.getText());
                kl.setJml_Siswa(txtJumlah.getText());
                DBConnection conn = DBConnection.getInstance();
                KelasQuery dao = new KelasQuery(conn.getCon());
                dao.insert(kl);
                JOptionPane.showMessageDialog(this, "Data Saved");
                loadRecords();
                updateRow();
            } catch (SQLException ex) {
                Logger.getLogger(FormKelas.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                Kelas kl = new Kelas();
                kl.setKode_kelas(txtKode.getText());
                kl.setNama_kelas(txtNama.getText());
                kl.setJurusan(txtJurusan.getText());
                kl.setKetua(txtKetua.getText());
                kl.setWali(txtWali.getText());
                kl.setJml_Siswa(txtJumlah.getText());
                DBConnection conn = DBConnection.getInstance();
                KelasQuery dao = new KelasQuery(conn.getCon());
                dao.update(kl.getKode_kelas(), kl);
                JOptionPane.showMessageDialog(this, "Data Update");
                loadRecords();
                updateRow();
            } catch (SQLException ex) {
                Logger.getLogger(FormKelas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

